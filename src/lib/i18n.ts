export function detectLocale(input?: string | null): Locale {
  const short = (input || "en").toLowerCase().split("-")[0];
  return (SUPPORTED_LOCALES as readonly string[]).includes(short) ? (short as Locale) : "en";
}

// Every visible string is fully translated per language. `extra` carries the
// wallet/swap-status strings for all non-en/es locales; merged last so there is no
// English fallback. (messages.en only seeds the type / key set.)
export function getMessages(locale: Locale): typeof messages.en {
  return {
    ...messages.en,
    ...(messages[locale] as Partial<typeof messages.en>),
    ...((extra[locale] as Partial<typeof messages.en>) ?? {}),
  };
}
